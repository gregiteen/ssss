#!/usr/bin/env node
/**
 * Reads the findings JSON written by probe-rbac.mjs and converts every
 * FAIL-OPEN finding into a conformance/fixtures.json-shaped fixture object,
 * ready to review and append — so a confirmed gap becomes a permanent
 * regression test instead of a one-time report.
 *
 * Usage: node emit-negative-fixtures.mjs [path-to-findings.json]
 * Defaults to the temp path probe-rbac.mjs writes to.
 */
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import crypto from 'node:crypto';

const findingsPath = process.argv[2] || path.join(os.tmpdir(), 'rbac-audit-findings.json');

if (!fs.existsSync(findingsPath)) {
  console.error(`❌ No findings file at ${findingsPath}. Run probe-rbac.mjs first.`);
  process.exit(1);
}

const findings = JSON.parse(fs.readFileSync(findingsPath, 'utf8'));

if (!findings.failOpen || findings.failOpen.length === 0) {
  console.log('✅ No fail-open findings to convert — nothing to emit.');
  process.exit(0);
}

function toFixture(finding, index) {
  const env = finding.envelope;
  return {
    id: `rbac-regression-${String(index + 1).padStart(3, '0')}`,
    name: `[GENERATED] ${finding.scenario} — must be denied`,
    description: `Auto-generated by rbac-fail-open-auditor from a CONFIRMED fail-open finding: ${finding.note}`,
    request: { ...env, idempotency_key: crypto.randomUUID() },
    expected_response: {
      success: false,
      validation: { valid: false },
    },
    expected_http_status: 422,
    _generated_by: 'rbac-fail-open-auditor/scripts/emit-negative-fixtures.mjs',
    _source_scenario: finding.scenario,
  };
}

const fixtures = findings.failOpen.map(toFixture);

console.log(`Generated ${fixtures.length} regression fixture(s) from confirmed fail-open finding(s):\n`);
console.log(JSON.stringify(fixtures, null, 2));
console.log(
  `\nReview each one, then append to conformance/fixtures.json's "fixtures" array ` +
  `(renumber the "id" to the next real fixture-NNN and drop the _generated_by/_source_scenario ` +
  `metadata keys — those are audit-trail only, not part of the fixture schema).`
);
